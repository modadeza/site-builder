/**
 * フィールドの条件付き表示判定
 */
export function shouldShowField(field, vals) {
  if (!field.showIf) return true
  if (field.showIf.includes(':')) {
    const [k, expected] = field.showIf.split(':')
    return String(vals[k]) === expected
  }
  return !!vals[field.showIf]
}

